// ES6 made classes in JavaScript, but they are just syntactical sugar over the existing prototype-based inheritance and constructor functions.
// Classes in JavaScript are a way to create objects and deal with inheritance in a more elegant and cleaner way. They are not a new object-oriented programming paradigm, but rather a new syntax for creating objects and dealing with inheritance in JavaScript.

class User {
    constructor(username, email, password){
        this.username = username;
        this.email = email;
        this.password = password
    }

    encryptPassword(){
        return `${this.password}abc`
    }
    changeUsername(){
        return `${this.username.toUpperCase()}`
    }

}

const chai = new User("chai", "chai@gmail.com", "123")

console.log(chai.encryptPassword());
console.log(chai.changeUsername());

// behind the scene

function User(username, email, password){
    this.username = username;
    this.email = email;
    this.password = password
}

//function getting injected into the prototype of User
User.prototype.encryptPassword = function(){
    return `${this.password}abc`
}
User.prototype.changeUsername = function(){
    return `${this.username.toUpperCase()}`
}


const tea = new User("tea", "tea@gmail.com", "123")

console.log(tea.encryptPassword());
console.log(tea.changeUsername());